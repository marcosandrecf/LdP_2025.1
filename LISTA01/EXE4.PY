# 4) Faça um programa que pergunte o número de horas trabalhadas no mês e o valor recebido
# por hora. O programa deve calcular e exibir o salário total do mês.

hTrab = int(input("Digite o numero de horas trabalhadas no mês: "))

hSal = float(input("Digite o valor da hora trabalhada: "))

Sal = hTrab * hSal

print("O salário do funcionario no mês é: R$", Sal)
